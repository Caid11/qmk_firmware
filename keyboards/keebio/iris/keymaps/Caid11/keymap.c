bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case SYMBOL:
      if (record->event.pressed) {
        layer_on(_SYMBOL);
        update_tri_layer(_SYMBOL, _TXTNAV, _FN);
      } else {
        layer_off(_SYMBOL);
        update_tri_layer(_SYMBOL, _TXTNAV, _FN);
      }
      return false;
      break;
    case TXTNAV:
      if (record->event.pressed) {
        layer_on(_TXTNAV);
        update_tri_layer(_SYMBOL, _TXTNAV, _FN);
      } else {
        layer_off(_TXTNAV);
        update_tri_layer(_SYMBOL, _TXTNAV, _FN);
      }
      return false;
      break;
    case FN:
      if (record->event.pressed) {
        layer_on(_FN);
      } else {
        layer_off(_FN);
      }
      return false;
      break;
  }
  return true;
}
